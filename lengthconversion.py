#this program contains modules which can be used to convert length into various units

def miletokm(mile):
    
    km = mile * 1.609344

    print("It is",km,"km's")

def kmtomile(km):

    mile = km / 1.609344
    print("It is",mile,"miles's")


def feettoinches(feet):
    
    inches = feet * 12

    print("It is",inches,"inches's")

def inchestofeet(inches):

    feet= inches / 12
    print("It is",feet,"feet")

